package org.example.gatewayservice.dto.subject;

public class SubjectRequestDTO {
    private String id;
    private String name;
    private int coeff;


    public SubjectRequestDTO() {
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCoeff() {
        return coeff;
    }

    public void setCoeff(int coeff) {
        this.coeff = coeff;
    }
}
